export function getMonsterForLesson(lessonId: string): KawaiiMonster {
  const numericSeed = lessonId
    .split('')
    .reduce((total, char) => total + char.charCodeAt(0), 0)

  return KAWAII_MONSTERS[numericSeed % KAWAII_MONSTERS.length]
}